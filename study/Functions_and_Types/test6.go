package main

import "fmt"

func main() {
	ns := []int{10, 20, 30, 40, 50}
	n, m := 2, 4
	fmt.Println(ns[n:]) // [30 40 50]
	fmt.Println(ns[:m]) // [10 20 30 40]
	fmt.Println(ns[:m:m])
	ms := ns[:m:m]
	fmt.Println(cap(ms))
}
